    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
   int c;
      try
      {
     Class.forName("com.mysql.jdbc.Driver");
      Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/library_management_system","root","fardzs201012");
      PreparedStatement insert=con.prepareStatement("SELECT * FROM `booklist`");
      java.sql.ResultSet rs=insert.executeQuery();
      ResultSetMetaData Rsmd=rs.getMetaData();
      int cc=Rsmd.getColumnCount();
      DefaultTableModel Dft=(DefaultTableModel)tbl1.getModel();
      Dft.setRowCount(0);
      
      while(rs.next())
      {
          Vector v2=new Vector();
          
          for(int i=0;i<=cc;i++)
          {
              v2.add(rs.getString("Book_ID"));
              v2.add(rs.getString("Name"));
              v2.add(rs.getString("Author"));
              v2.add(rs.getString("Publisher"));
           
          }
          
          Dft.addRow(v2);
      }
      
      JOptionPane.showMessageDialog(null,"Connected");
    
      
      }
      
      catch(Exception e)
      {
          JOptionPane.showMessageDialog(null," Not Connected");
      }           // TODO add your handling code here:
     
    }//GEN-LAST:event_jButton2ActionPerformed

